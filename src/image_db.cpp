image_db::image_db()
{
	static bool is_generate = true;
	if (is_generate) {
		generate();
		is_generate = false;
	}
}

std::vector<std::vector<unsigned>> image_db::get_image_by_name(const String& str)
{
	if (database.find(str) == database.end()) {
		// not fount
		return{};
	}
	return database[str];
}

